create view vw_bi_avaliacoes_area as
select
	a.id,
	a.descricao,
	a.nota,
	a2.id as area_id
from
	avaliacao a
join solicitacao s on
	s.avaliacao_id = a.id
join servico s2 on
	s.servico_id = s2.id
join area a2 on
	a2.id = s2.area_id ;
